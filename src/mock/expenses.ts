// --- Summary calculations ---
export const expenseSummary = {
    totalExpenses: expenses.reduce((s, e) => s + e.amount, 0),
    approved: expenses.filter(e => e.status === 'approved').reduce((s, e) => s + e.amount, 0),
    pending: expenses.filter(e => e.status === 'pending').reduce((s, e) => s + e.amount, 0),
    rejected: expenses.filter(e => e.status === 'rejected').reduce((s, e) => s + e.amount, 0),
    draft: expenses.filter(e => e.status === 'draft').reduce((s, e) => s + e.amount, 0),
    byCategory: EXPENSE_CATEGORIES.map(cat => ({
        category: cat.label,
        amount: expenses.filter(e => e.category === cat.value).reduce((s, e) => s + e.amount, 0),
        count: expenses.filter(e => e.category === cat.value).length,
    })).filter(c => c.count > 0).sort((a, b) => b.amount - a.amount),
    byDepartment: DEPARTMENTS.map(dept => ({
        department: dept,
        amount: expenses.filter(e => e.department === dept).reduce((s, e) => s + e.amount, 0),
        count: expenses.filter(e => e.department === dept).length,
    })).filter(d => d.count > 0).sort((a, b) => b.amount - a.amount),
    monthlyTotals: (() => {
        const map: Record<string, number> = {};
        expenses.forEach(e => {
            const key = e.date.slice(0, 7); // YYYY-MM
            map[key] = (map[key] || 0) + e.amount;
        });
        return Object.entries(map).sort(([a], [b]) => a.localeCompare(b)).map(([month, amount]) => ({ month, amount }));
    })(),
};

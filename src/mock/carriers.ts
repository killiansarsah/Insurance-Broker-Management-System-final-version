export type CarrierType = 'non-life' | 'life' | 'reinsurer';
export type CarrierStatus = 'active' | 'suspended' | 'inactive';

export interface Carrier {
    id: string;
    slug: string;
    name: string;
    shortName: string;
    type: CarrierType;
    licenseNumber: string;
    status: CarrierStatus;
    established: number;
    website: string;
    /** Clearbit URL, or '' to use brandColor initials avatar */
    logoUrl: string;
    /** Hex brand colour for avatar fallback */
    brandColor: string;
    description: string;
    parentGroup?: string;
    hq: string;
    phone: string;
    email: string;
    contactPerson: string;
    /** Revenue rank among non-life companies (InsureGhana 2024), 0 if life/reinsurer */
    revenueRank: number;
    /** 2024 Insurance Revenue in GHS thousands */
    revenue2024?: number;
    /** Solvency / credit rating if available */
    rating?: string;
    /** Product categories this carrier offers */
    productCategories: string[];
    /** Commission rates by product category */
    commissionRates: Record<string, number>;
}


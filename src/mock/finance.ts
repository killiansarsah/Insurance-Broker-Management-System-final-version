export interface Invoice {
    id: string;
    invoiceNumber: string;
    clientId: string;
    clientName: string;
    policyId: string;
    policyNumber: string;
    description: string;
    amount: number;
    amountPaid: number;
    status: 'paid' | 'outstanding' | 'overdue' | 'partial' | 'cancelled';
    dateIssued: string;
    dateDue: string;
    datePaid?: string;
    currency: 'GHS' | 'USD' | 'EUR';
    notes?: string;
}

